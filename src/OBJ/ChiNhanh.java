package OBJ;

/**
 *
 * @author HuyChu
 */
public class ChiNhanh {

    private Long id;
    private String ma;
    private String ten;
    private String moTa;
    private String viTri;

    public ChiNhanh(Long id) {
        this.id = id;
    }

    public ChiNhanh(String ten) {
        this.ten = ten;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }

    public String getViTri() {
        return viTri;
    }

    public void setViTri(String viTri) {
        this.viTri = viTri;
    }

}
